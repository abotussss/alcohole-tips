const expandSakeBrandLineup = (brand: SakeBrand): SakeBrand => {
  const additions = [
    ...(sakeBrandLineupAdditions[brand.slug] ?? []),
    ...(prefectureWideSakeLineupAdditions[brand.slug] ?? []),
  ];

  if (!additions.length) {
    return brand;
  }

  return {
    ...brand,
    lineup: [...brand.lineup, ...additions],
  };
};

